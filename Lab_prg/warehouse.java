package supplyChainManagement;

public class Warehouse {

	private String Address;
	private double Phone_number;
	private int No_of_Category;
	private int No_of_stock;
	private int No_of_security;
	private int Expected_stock;
	private float Available_space;
	private int No_of_workers;
	private float Total_space;

	public String getAddress() {
		return Address;
	}
	public void setAddress(String address) {
		Address = address;
	}
	public double getPhone_number() {
		return Phone_number;
	}
	public void setPhone_number(double phone_number) {
		Phone_number = phone_number;
	}
	public int getNo_of_Category() {
		return No_of_Category;
	}
	public void setNo_of_Category(int no_of_Category) {
		No_of_Category = no_of_Category;
	}
	public int getNo_of_stock() {
		return No_of_stock;
	}
	public void setNo_of_stock(int no_of_stock) {
		No_of_stock = no_of_stock;
	}
	public int getNo_of_security() {
		return No_of_security;
	}
	public void setNo_of_security(int no_of_security) {
		No_of_security = no_of_security;
	}
	public int getExpected_stock() {
		return Expected_stock;
	}
	public void setExpected_stock(int expected_stock) {
		Expected_stock = expected_stock;
	}
	public float getAvailable_space() {
		return Available_space;
	}
	public void setAvailable_space(float available_space) {
		Available_space = available_space;
	}
	public int getNo_of_workers() {
		return No_of_workers;
	}
	public void setNo_of_workers(int no_of_workers) {
		No_of_workers = no_of_workers;
	}
	public float getTotal_space() {
		return Total_space;
	}
	public void setTotal_space(float total_space) {
		Total_space = total_space;
	}
}

